/*
 * Conversion of ADC data to float.
 * Scaling to [-1,1] for LMS filtering
 */
void float_conversion(uint16_t* adc_buf, float32_t* temp_buf, uint32_t len,uint8_t channel){
	for (int i = 0; i < len; i++){
		temp_buf[i] = ((float32_t)adc_buf[2*i + channel] - 2048.0f) / 2048.0f;
	}
}
